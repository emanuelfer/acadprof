package model;

/**
 *
 * @author emanu
 */
public class Aluno {
    private int id;
    private String nome;
    private String matricula;
    private String CPF;
    private Turma turma;

    public Aluno() {
    }

    public Aluno(int id, String nome, String matricula, String CPF, Turma turma) {
        this.id = id;
        this.nome = nome;
        this.matricula = matricula;
        this.CPF = CPF;
        this.turma = turma;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getMatricula() {
        return matricula;
    }

    public void setMatricula(String matricula) {
        this.matricula = matricula;
    }

    public String getCPF() {
        return CPF;
    }

    public void setCPF(String CPF) {
        this.CPF = CPF;
    }

    public Turma getTurma() {
        return turma;
    }

    public void setTurma(Turma turma) {
        this.turma = turma;
    }
    
    
}
